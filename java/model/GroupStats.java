package model;

public class GroupStats {
	double total;
	double totaluserpaid;
	double totalusershare;
	public double getTotal() {
		return total;
	}
	public void setTotal(double total) {
		this.total = total;
	}
	public double getTotaluserpaid() {
		return totaluserpaid;
	}
	public void setTotaluserpaid(double totaluserpaid) {
		this.totaluserpaid = totaluserpaid;
	}
	public double getTotalusershare() {
		return totalusershare;
	}
	public void setTotalusershare(double totalusershare) {
		this.totalusershare = totalusershare;
	}
}
